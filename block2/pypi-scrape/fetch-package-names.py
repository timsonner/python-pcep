"""
Program to fetch the list of package names from PyPI and output just the names.
package list: https://pypi.org/simple/
package info: https://pypi.org/pypi/{package_name}/json

example output: https://pypi.org/simple/ contains a list of package names in the format:
<a href="../../project/absl-py/">absl-py</a><br/>
"""

