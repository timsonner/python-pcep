# Compilation: Translates entire source program to machine code or bytecode before execution.

# Interpretation: Executes source program directly, translating and executing statements one at a time

# Lexis: The tokens that make up the language such as keywords, operators, and identifiers.

# Syntax: The grammar rules for forming valid statements

# Semantics: The meaning of the statements